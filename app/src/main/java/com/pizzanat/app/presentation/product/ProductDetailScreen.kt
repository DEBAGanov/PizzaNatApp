@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun ProductDetailScreen(
    productId: Long = 0L,
    onNavigateBack: () -> Unit = {},
    onNavigateToCart: () -> Unit = {},
    viewModel: ProductDetailViewModel = hiltViewModel()
) {
    val uiState by viewModel.uiState.collectAsStateWithLifecycle()
    val scrollState = rememberScrollState()
    
    // –ó–∞–≥—Ä—É–∂–∞–µ–º –ø—Ä–æ–¥—É–∫—Ç –ø—Ä–∏ –ø–µ—Ä–≤–æ–π –∑–∞–≥—Ä—É–∑–∫–µ —ç–∫—Ä–∞–Ω–∞
    LaunchedEffect(productId) {
        if (productId > 0L) {
            viewModel.loadProduct(productId)
        }
    }

    Box(
        modifier = Modifier.fillMaxSize()
    ) {
        // –°–µ—Ä—ã–π —Ñ–æ–Ω –≤ —Å—Ç–∏–ª–µ Fox Whiskers
        Column(
            modifier = Modifier
                .fillMaxSize()
                .background(MaterialTheme.colorScheme.background)
                .statusBarsPadding()
        ) {
            when {
                uiState.isLoading -> {
                    LoadingContent()
                }
                
                uiState.error != null -> {
                    ErrorContent(
                        error = uiState.error ?: "–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –æ—à–∏–±–∫–∞",
                        onRetry = { viewModel.loadProduct(productId) },
                        onNavigateBack = onNavigateBack
                    )
                }
                
                uiState.product == null -> {
                    NotFoundContent(onNavigateBack = onNavigateBack)
                }
                
                else -> {
                    ProductContent(
                        product = uiState.product!!,
                        uiState = uiState,
                        onNavigateBack = onNavigateBack,
                        onNavigateToCart = onNavigateToCart,
                        onAddToCart = viewModel::addToCart,
                        onHideSuccess = viewModel::hideAddToCartSuccess
                    )
                }
            }
        }

        // Floating –∫–Ω–æ–ø–∫–∞ –∫–æ—Ä–∑–∏–Ω—ã —Ç–æ–ª—å–∫–æ –∫–æ–≥–¥–∞ –ø—Ä–æ–¥—É–∫—Ç –∑–∞–≥—Ä—É–∂–µ–Ω
        if (uiState.product != null) {
            FloatingCartButton(
                onNavigateToCart = onNavigateToCart,
                modifier = Modifier.align(Alignment.BottomCenter)
            )
        }
    }
}

