@Composable
private fun ProductContent(
    product: Product,
    uiState: ProductDetailUiState,
    onNavigateBack: () -> Unit,
    onNavigateToCart: () -> Unit,
    onAddToCart: () -> Unit,
    onHideSuccess: () -> Unit
) {
    Column(
        modifier = Modifier.fillMaxSize()
    ) {
        // –ñ–µ–ª—Ç–∞—è –ø–ª–∞—à–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–∞ –∫–∞–∫ –≤ Fox Whiskers
        ProductTopBar(
            productName = product.name,
            onNavigateBack = onNavigateBack,
            onNavigateToCart = onNavigateToCart
        )
        
        // Content —Å –ø—Ä–æ–∫—Ä—É—Ç–∫–æ–π
        Column(
            modifier = Modifier
                .fillMaxSize()
                .verticalScroll(rememberScrollState())
                .padding(16.dp),
            verticalArrangement = Arrangement.spacedBy(16.dp)
        ) {
            // –ö—Ä—É–≥–ª–æ–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ –ø—Ä–æ–¥—É–∫—Ç–∞ –≤ —Ü–µ–Ω—Ç—Ä–µ
            Row(
                modifier = Modifier.fillMaxWidth(),
                horizontalArrangement = Arrangement.Center
            ) {
                FoxCircularProductImageLarge(
                    imageUrl = product.imageUrl,
                    contentDescription = product.name,
                    size = 200.dp
                )
            }
            
            // –ö–∞—Ä—Ç–æ—á–∫–∞ —Å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–µ–π –æ –ø—Ä–æ–¥—É–∫—Ç–µ
            ProductInfoCard(product = product)
            
            // –ö–∞—Ä—Ç–æ—á–∫–∞ —Å –æ–ø–∏—Å–∞–Ω–∏–µ–º (–µ—Å–ª–∏ –µ—Å—Ç—å)
            if (!product.description.isNullOrBlank()) {
                ProductDescriptionCard(description = product.description)
            }
            
            // –ö–Ω–æ–ø–∫–∞ –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –≤ –∫–æ—Ä–∑–∏–Ω—É
            AddToCartButton(
                isLoading = uiState.isAddingToCart,
                onAddToCart = onAddToCart
            )
            
            // –°–æ–æ–±—â–µ–Ω–∏–µ –æ–± —É—Å–ø–µ—à–Ω–æ–º –¥–æ–±–∞–≤–ª–µ–Ω–∏–∏
            SuccessMessage(
                showSuccess = uiState.showAddToCartSuccess,
                onHideSuccess = onHideSuccess
            )
            
            // –û—Ç—Å—Ç—É–ø —Å–Ω–∏–∑—É –¥–ª—è floating –∫–Ω–æ–ø–∫–∏
            Spacer(modifier = Modifier.height(80.dp))
        }
    }
}

